        console.log(e.target.files);
        var uploadData = new FormData();
        uploadData.append('i_folder_id', $("i_folder_id").val());
        uploadData.append('uploads', e.target.files);
        var url = ADMIN_URL+'upload-files';
        $.ajax({
            url: url,
            type: 'POST',
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            },
            data: uploadData,
            success: function (data) {
                // console.log(data)
            },
            cache: false,
            processData: false
        });
    });
